class Ocarina {
    private String color;
    private String song;
    
        //getter for color
    public String getColor(){
        return color;
    }
        //setter for color
    public void setColor(String c) {
        this.color = c; 
    }
        //getter for song
    public String getSong(){
        return song;
    }
        //setter for song
    public void setSong(String s) {
        this.song = s; 
    }
}
